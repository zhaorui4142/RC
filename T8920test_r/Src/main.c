//计算是否超时
bool CheckTimeout(uint32_t start_time, uint32_t timeout)
{
    uint32_t tick = HAL_GetTick();
    uint32_t ellipsed;
    if(tick >= start_time)
        ellipsed =  tick - start_time;
    else
        ellipsed =  (0xFFFFFFFF - start_time) + tick;
    
    if(ellipsed >= timeout)
        return true;
    else
        return false;
}
